var MapChickEvent=function(type,pixel,lolat){
    /**
     * 事件类型
     * @type {String}
     */
    this.type=type;
     /**
     * 像素坐标
     * @type {Array}
     */
    this.pixel=pixel;
     /**
     * 地理坐标
     * @type {Array}
     */
    this.lolat=lolat;
};
/**
 * map点击事件
 * @constant
 * @type String
 */
MapChickEvent.MAP_CLICK = "mapClick";